<template>
  <div id="app">
    <h1>Viinit</h1>
    <wine-form @add:wine="addWine"/>
    <wine-table :wines="wines" @delete:wine="deleteWine" @edit:wine="editWine"/>
  </div>
</template>

<script>
import axios from "axios"
import WineForm from "@/components/WineForm.vue"
import WineTable from "@/components/WineTable.vue"
import { WineService } from "@/services/wineService.js";
import { wineStore } from "@/stores/wineStore.js";

const wineService = new WineService(wineStore);

export default {
  name: "app",
  components: {
    WineForm,
    WineTable
  },
  data() {
    return {
      wines: wineStore.state.wines
    }
  },
  mounted() {
    this.getWines();
  },
  // TODO: Move CRUD methods to service component.
  methods: {
    getWines() {
      axios.get("http://localhost:8080/api/wines/")
          .then(response => this.wines = response.data)
          .catch(error => console.log(error));
      
        // wineService.getWines();
        // console.log(this.wines);
    },
    getWine(id) {
      // axios.get("http://localhost:8080/api/wines/" + id)
      //   .then((wine) => console.log(wine))
      //   .catch((error) => console.log(error));

      wineService.getWine(id);
    },
    async addWine(wine) {
      // TODO: remove ID generation. IDs are generated by the database.
      const lastId = this.wines.length > 0 ? this.wines[this.wines.length - 1].id : 0;
      const id = lastId + 1;
      const newWine = {...wine, id};
      this.wines = [...this.wines, newWine];

      axios.post("http://localhost:8080/api/wines", wine)
        .then((newWine) => this.wines = [...this.wines, newWine])
        .catch((error) => console.log(error));
    },
    async editWine(id, editedWine) {
      // this.wines = this.wines.map(wine => wine.id === id ? editedWine : wine);

      axios.put("http://localhost:8080/api/wines/" + id, editedWine)
        .then((response) => this.wines.map(wine => wine.id === id ? response : wine))
        .catch((error) => console.log(error));
    },
    deleteWine(id) {
      // this.wines = this.wines.filter(wine => wine.id !== id);

      axios.delete("http://localhost:8080/api/wines/" + id)
        .then(() => this.wines.filter(wine => wine.id !== id))
        .catch((error) => console.log(error));
    }
  }
}
</script>

